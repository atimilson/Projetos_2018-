    private void btncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarActionPerformed
        ClienteController cliController = new ClienteController();

        cliController.cliente.setNome(jTextNome.getText());
        cliController.cliente.setCpf(jTextcpf.getText());
        cliController.cliente.setRg(jTextrg.getText());
        cliController.cliente.setTelefone(jTexttelefone.getText());
        cliController.cliente.setEndereco(jTextendereco.getText());
        cliController.cliente.setNome(jTextNome.getText());
        cliController.cliente.setCpf(jTextcpf.getText());
        cliController.cliente.setRg(jTextrg.getText());
        cliController.cliente.setTelefone(jTexttelefone.getText());
        cliController.cliente.setEndereco(jTextendereco.getText());

        if (idEditar == 0) {

            verificaCamposEmpty();
            dispose();
        } else {
            cliController.editar(cliController.cliente, idEditar);
            dispose();
        }

    }//GEN-LAST:event_btncadastrarActionPerformed

    public void salvar() {
        ClienteController controller = new ClienteController();

        controller.cliente.setNome(jTextNome.getText());
        controller.cliente.setCpf(jTextcpf.getText());
        controller.cliente.setRg(jTextrg.getText());
        controller.cliente.setTelefone(jTexttelefone.getText());
        controller.cliente.setEndereco(jTextendereco.getText());

        try {
            controller.salvar(controller.cliente);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "error: " + ex);
        } finally {

        }
    }


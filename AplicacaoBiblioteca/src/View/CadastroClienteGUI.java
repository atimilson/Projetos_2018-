    private void verificaCamposEmpty() {

        if ((jTextNome.getText().trim().length() > 0) || !(jTextNome.getText().trim().length() <= 4)) {
            if (!jTextcpf.getText().equals("   .   .   -  ")) {
                if (jTextendereco.getText().trim().length() > 0) {
                    if (!jTextrg.getText().trim().equals("-")) {
                        if (!jTexttelefone.getText().equals("(  )     -    ")) {
                            salvar();
                        } else {
                            lbObrigatorioTel.setVisible(true);
                        }
                    } else {
                        lbObrigatorioRg.setVisible(true);
                    }
                } else {
                    lbObrigatorioEndereco.setVisible(true);
                }
            } else {
                lbObrigatorioCpf.setVisible(true);
            }
        } else {
            lbObrigatorioNome.setVisible(true);
        }
    }


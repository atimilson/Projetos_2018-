    private void verificaCamposEmpty() {

        if (jTextNome.getText().length() > 0) {
            lbObrigatorioNome.setVisible(false);
        } else {
            lbObrigatorioNome.setVisible(true);
        }
        if (jTextcpf.getText().length() > 0) {
            lbObrigatorioCpf.setVisible(false);
        } else {
            lbObrigatorioCpf.setVisible(true);
        }
        if (jTextendereco.getText().length() > 0) {
            lbObrigatorioEndereco.setVisible(false);
        } else {

            lbObrigatorioEndereco.setVisible(true);
        }
        if (jTextrg.getText().length() > 0) {
            lbObrigatorioRg.setVisible(false);
        } else {
            lbObrigatorioRg.setVisible(true);

            if (jTexttelefone.getText().length() > 0) {
                lbObrigatorioTel.setVisible(false);
            } else {
                lbObrigatorioTel.setVisible(true);

            }
            

        }

    }


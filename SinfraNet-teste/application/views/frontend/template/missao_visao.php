    <P class="titulo-missao"><b>TRANSFORMANDO AS ESTRADAS DE MATO GROSSO:</b></P>
    <p class="subtitulo-missao"><b>Missão:</b>“Garantir um sistema de transporte seguro, confiável, integrado e eficiente, contribuindo para a melhoria da qualidade de vida e o desenvolvimento sustentável do Estado de Mato Grosso”.</p>
    <p class="subtitulo-missao"><b>Visão:</b> “Ser reconhecida nacionalmente pela excelência na gestão e evolução do sistema de transporte estadual”.</p>
    <p class="subtitulo-missao"><b>Ética:</b> Agir pautado nos princípios da Moralidade e da Legalidade;</p>
    <p class="subtitulo-missao"><b>Transparência:</b> Promover ações que facilitem o acesso a informação e dos resultados estabelecidos.</p>
    <p class="subtitulo-missao"><b>Valorização das Pessoas:</b>Propiciar condições eficazes para o desempenho, o desenvolvimento das competências e a satisfação no ambiente de trabalho.</p>
    <p class="subtitulo-missao"><b>Comprometimento: </b>Ter foco na melhoria contínua da SINFRA a partir da sua contribuição individual, identificando o problema e apresentando soluções orientadas para resultados;</p>
    <p class="subtitulo-missao"><b>Qualidade:</b> Agir com conhecimento técnico para assegurar a eficiência e eficácia na aplicação dos recursos e nos resultados Institucionais;</p>
